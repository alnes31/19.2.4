class Calculator:

   # функция умножения
   def multiply(self, x, y):
       return x * y

   # функция деления
   def division(self, x, y):
       return x / y


   # функция вычитания
   def subtraction(self, x, y):
       return x - y

   # функция сложения
   def adding(self, x, y):
       return x + y